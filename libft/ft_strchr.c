char	*ft_strchr(const char *s, int c)
{
	char	i;

	i = c;
	while (*s != '\0')
	{
		if (*s == i)
			return ((char *)s);
		s++;
	}
	if (s == 0 || i == 0)
		return ((char *)s);
	return (0);
}
